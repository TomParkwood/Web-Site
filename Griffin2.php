         <td style = "width:70%;">The Grifin 2 range of A3 Colour devices offer a range of options and upgrades making it adaptable to suit your current and future needs. Ideal for any busy workgroup that needs high performance, high quality and versatility.<br>
         The standard print release function allows users to send and store documents to a main MFP, which then acts as a document server which allows for the print jobs to be securely released at one of up to five connected MFPs. Workflow enhancements which include single Sign-On cloud connectivity, mobile connectivity via Wi-Fi and USB printing of MS Office Files.<br>
         A tiltable 10.1" colour LCD touchscreen provides the ability to drag and drop frequently used icons for simple customisation of the home screen. Everyday operation is simple and intuitive; just touch, tap, pinch, spread or swipe for instant access to every feature, function and setting including an advanced preview feature.<br>
         Print and copy speeds of up to 40 pages per minute in colour and black &amp; white, a built in 100- sheet reversing single pass feeder and a scan speed of up to 80 ipm, all of which help to boost efficiency and productivity. Using the 600 x 600 dpi print resolution combined with Sharp's auto process control and developer refresh ensures a consistently high image quality.<br>
         A maximum paper capacity of 6,300 sheets and support a wide range of media (up to SRA3 and 300gsm) which enables the device to handle varied output requirements with ease. And a varied selection of finishing options such as saddle stiching, manual and stapleless stapling to add further versatility.<br>
         The Energy Star certified devices uses Sharp's latest energy-saving technology. With a rapid warm up time you can be printing in 20 seconds and copying in just 6.7 seconds. With low power consumption and low TEC value all aim to save money and support your environmental objectives. In addition, the automatic toner cartridge eject function ensures that all toner has been consumed before it can be replaced, therby saving resources and saving you money by not wasting toner.</td>
