      It does not need to be connected to any other device - such as a PC, laptop or projector used in the past - but may be used entirely independantly.<br>
      The image remains crisp and sharp - and does not fade - over a life expectancy of a massive 50,000 hours of use. And since it consumes so little energy, it is cheap to run, too.<br>
